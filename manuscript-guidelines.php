					<h3 class="">Manuscript     
						<span class="text-success">Titles </span>
					</h3>
					<div class="lis-sty-none">
						<p>Tile should be brief and precise technical enough to explain the content it holds in the manuscript. It is usually free from acronyms, abbreviations or punctuations.</p>
					</div>
					<br>
					<h3 class="">Author(s)     
						<span class="text-success"> </span>
					</h3>
					<div class="lis-sty-none">
						<p>Author names need to be mentioned as Last name followed by their concern Initials without space or any punctuation. In case of more than one author is present they should be differentiated by the conjunction ‘and’ in between. Their affiliations are numbered after the initials in superscript. Corresponding author is represented with a superscript star.</p>
						<p>Eg: Peter SG 1, Sudan HS 2 and Gracia LI 2 *</p>
						<p ><strong> Affiliation(s):</strong>  Author Affiliations must be provided as: Department, University, Country. email:</p>
						<p>Eg: Peter SG 1, Sudan HS 2 and Gracia LI 2 *</p>
						<p>1Department of Cardiovascular Medicine, University of California, USA. email:peter@university.com</p>
						<p>2Department of Cardiology and Surgery, University of Toronto, Canada. email:gracia@university.com</p>
						
						<p ><strong> *Corresponding author: </strong>  Last Name followed by Initials, Department, University, Complete Postal Address, Country, Telephone details; Fax No., Email.</p>
						<p>Eg: Christy LI, Department of Cardiology and Surgery, University of Toronto, 172 St George St, Toronto, ON M5R 0A3, Canada, Tel: 23-456-7890; Fax: 123-456-7890; Email: christy@university.com</p>
						<h4 class="">Abstract</h4>
						<p >The word limit for Abstract is 250 or fewer words. Abstract must summarize specifically the scientific content in the manuscript. Abstracts are short and accurate. Abstracts must be free from citations.</p>
						<h4 class="">Keywords</h4>
						<p >Keywords serve as main tool for Indexers as well as Readers to locate your manuscript. Keywords enhance the chances of getting more citations and high readership to your manuscript. An article might contain up to maximum of 4-6 Keywords.</p>
						<p>Eg: Lung cancer; Biomarkers; Cancer therapy</p>
						<h4 class="">Abbreviations</h4>
						<p >Abbreviations must be separated by semicolon and all the abbreviations used in the manuscript must be mentioned.</p>
						<h4 class="">Body Text</h4>
						<p >Respective Manuscript types must be written with the above mentioned structure format. Any of the mentioned structure missing will be resent to the author for further modifications.</p>
						
						<h4 class="">Figures & Tables</h4>
						<p >GRF Publishing Group requires tables and figures to be submitted at the end of the manuscript. Please ensure that all the figures submitted comply with our Image integrity.</p>
						<p >Figures submitted should be at least 300 dpi, submitted in Tiff, Giff or JPEG formats only. All the figures submitted must be compatible with clarity. It should be intelligible for the readers to understand related to all disciplines. Excessive colour and unnecessary details must be avoided. Allowable pixel size for the Figures is 800 pixels (Standard) and 1200 pixels (high resolution). All the figures submitted must be in standard or high resolution quality.</p>
						<p >Figure legends must be short and mentioned below each figure. The respective Figure numbers must be mentioned in the body of the manuscript sequentially within round brackets.</p>
						
						<h4 class="">Tables</h4>
						<p >GRF Publishing Group requires Tables to be small and presented in Table format. Pie diagrams, Charts etc are considered as "Figures". Tables presented in figure formats are not acceptable. Table legends must be mentioned above the Table. Tables must be provided at the end of the manuscript below figures. Symbols and Abbreviations used in the tables must be presented below the Table.</p>
						<p ><strong>*Note: </strong> Small table denotes that it should fit normally into the windows. Else it should be added as a supplementary table.</p>
						
						<h4 class="">Equations and Formulas</h4>
						<p >For mathematical symbols, Greek letters and other special characters, use normal text or Symbol font. MathType is used only for formulae that cannot be produced using normal text or Symbol font.</p>
						<p ><strong>Units: </strong>  SI/IU Units must be represented.</p>
						
						<h4 class="">Supplementary Information</h4>
						<p >Supplementary information is usually provided as an essential background to the Article such as any example, large data sets, methods, calculations, etc. If the manuscript contains any Supplementary data the authors are asked to refer the supplementary data at an appropriate point in the main body of the manuscript. The supplementary information should not contain any figures. It may have Supplementary tables. They are published as raw dataset along with the manuscript.</p>
						
						<h4 class="">References</h4>
						<p >GRF Publishing Group follows author-number system (Vancouver Style) where all the references including URLs are numbered successively within square brackets, in the same order of their appearance in the text. Examples are given below</p>
						<p ><strong>Journal Article with less than 6 authors:</strong> Karimi-Zarchi M, Peighmbari F, Karimi N, Rohi M, Chiti Z. A Comparison of 3 Ways of Conventional Pap Smear, Liquid-Based Cytology and Colposcopy vs Cervical Biopsy for Early Diagnosis of Premalignant Lesions or Cervical Cancer in Women with Abnormal Conventional Pap Test. Int J Biomed Sci. 2013; 9: 205-210.</p>		
						<p ><strong>Journal Article with more than 6 authors: </strong>Henriques A, Arantes-Rodrigues R, Faustino-Rocha IA, Teixeira-Guedes IC, Pinho-Oliveira J, Talhada D, et al. The Effects of Whole Green Tea Infusion on Mouse Urinary Bladder Chemical Carcinogenesis. Iran J Basic Med Sci. 2014; 17: 145-148.</p>
						<p ><strong>Book References: </strong>Gardner JG, Simmons MJ, Snustad PD. Principles of Genetics. 8th edn. New York: John Wiley & Sons. 2006</p>
						<p ><strong>Book Chapter:</strong> Honn KV, Tang DG, Chen Y. Adhesion molecules and site-specific metastasis. NeriSerneri SS, Gensini GF, Abbate R, Prisco D, editors. In: Thrombosis: An Update. Scientific Press. 1992; 269-303.</p>
						<p ><strong>Link/URL:</strong>  National Cancer Institute at National Institutes of Health. [hyperlinked with www.cancer.gov]</p>	
						<p ><strong> Proceedings of a Conference:</strong>  Gee JC, Joshi S, Pohl KM, Wells WM, Zollei L, editors. Information Processing in Medical Imaging. Proceedings of 23rd International Conference; 2013 June 28--July 3; CA, USA. New York: Springer, 2013.</p>	<p ><strong>PhD Theses/Dissertation:</strong>   Simonneau A. Gold-Catalyzed Cycloisomerization Reactions through Activation of Alkynes [dissertation]. Springer Theses, 2014.</p>
						<p ><strong>Datasets:</strong>  Zheng LY, Guo XS, He B, Sun LJ, Peng Y, Dong SS, et al. Genome data from sweet and grain sorghum (Sorghum bicolor). GigaScience. 2011.</p>
						
						<h4 class="">Journal Article Epub ahead of print</h4>
						<p>Reardon DA, Wen PY. Targeted therapies: Further delineating bevacizumab's response spectrum. Nat Rev ClinOncol. 2014 [In press].</p>	
						
						<h4 class="">Clinical Images</h4>
						<p>Clinical images are the pictures that show the clinical findings of a particular case of the patient. The clinical pictures should include questions and answers that should educate or remind readers about an important clinical situation or event. The image should not just describe the picture; however, should be informative enough from a clinical teaching view.</p>
						
						<h4 class="">Formatting guidelines</h4>
						<p>The title should be less than five to eight words. The text describing a clinical question should be within 150 words. The images can be uploaded in .tiff, .jpg or .jpeg format. We recommend using high-quality images. Text should be in one double-spaced electronic document.</p>
						
						<h4 class="">Patient Consent Form</h4>
						<p>If the image/figure of the patient is identifiable then the patient consent form is required. Incase, the eyes are masked then the form is not mandatory.</p>
						
						<h4 class="">Withdrawal Policy</h4>
						<p>An author is free to withdraw article at no charge-as long as it is withdrawn within a week of its original submission. However, once peer review is completed the paper is accepted for publication, and it has been worked into a journal issue, where we have incurred some costs from our end that need to be reimbursed. Hence author(s) are requested to pay about 25% of the publishing fee towards it for withdrawl.</p>
						
						

						
						
